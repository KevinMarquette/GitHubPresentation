# Welcome to the Git Workshop!

# Step 1: Initialize a new Git repository
Write-Host "Step 1: Initialize a new Git repository"
Write-Host "--------------------------------------"
Write-Host "To start, let's create a new Git repository in your current directory."
Write-Host "Run the following command to initialize a new repository:"
Write-Host "git init"

# Step 2: Add files to the repository
Write-Host "`nStep 2: Add files to the repository"
Write-Host "---------------------------------"
Write-Host "Now that you have a repository, let's add some files to it."
Write-Host "Use the following command to add a file to the repository:"
Write-Host "git add <file>"

# Step 3: Commit changes
Write-Host "`nStep 3: Commit changes"
Write-Host "---------------------"
Write-Host "After adding files, you need to commit your changes."
Write-Host "Run the following command to commit your changes:"
Write-Host "git commit -m 'Initial commit'"

# Step 4: Create a new branch
Write-Host "`nStep 4: Create a new branch"
Write-Host "---------------------------"
Write-Host "Branches allow you to work on different versions of your code simultaneously."
Write-Host "To create a new branch, use the following command:"
Write-Host "git branch <branch-name>"

# Step 5: Switch to a different branch
Write-Host "`nStep 5: Switch to a different branch"
Write-Host "-----------------------------------"
Write-Host "To switch to a different branch, use the following command:"
Write-Host "git checkout <branch-name>"

# Step 6: Merge branches
Write-Host "`nStep 6: Merge branches"
Write-Host "----------------------"
Write-Host "Once you're done working on a branch, you can merge it back into the main branch."
Write-Host "Use the following command to merge a branch:"
Write-Host "git merge <branch-name>"

# Step 7: Push changes to a remote repository
Write-Host "`nStep 7: Push changes to a remote repository"
Write-Host "--------------------------------------------"
Write-Host "To share your changes with others, you can push them to a remote repository."
Write-Host "Run the following command to push your changes:"
Write-Host "git push <remote-name> <branch-name>"

# Step 8: Pull changes from a remote repository
Write-Host "`nStep 8: Pull changes from a remote repository"
Write-Host "---------------------------------------------"
Write-Host "To get the latest changes from a remote repository, use the following command:"
Write-Host "git pull <remote-name> <branch-name>"

# Step 9: View commit history
Write-Host "`nStep 9: View commit history"
Write-Host "---------------------------"
Write-Host "To view the commit history of your repository, use the following command:"
Write-Host "git log"

# Step 10: Clone a repository
Write-Host "`nStep 10: Clone a repository"
Write-Host "--------------------------"
Write-Host "To create a local copy of a remote repository, use the following command:"
Write-Host "git clone <repository-url>"

# Congratulations!
Write-Host "`nCongratulations! You've completed the Git Workshop."
Write-Host "Feel free to explore more Git commands and features on your own!"